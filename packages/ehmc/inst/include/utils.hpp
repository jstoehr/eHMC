template<typename type_arma>
arma::uword sample_int_1(const type_arma& prob) {
  double u = arma::randu();
  double p = prob.at(0);
  arma::uword i = 0;
  while (u > p and i < prob.n_elem) {
    i++;
    p += prob.at(i);
  }
  return i;
}

