template<type_metric>
void inplace_tri_mat_mult(const arma::mat& trimat,
                          arma::vec& x) {
  /* C++ Armadillo version of the dtrmv BLAS function */
  // --- Return t(x) * upper_tri
  for(arma::uword j = trimat.n_cols; j-- > 0;) {
    double tmp = 0.0;
    for(arma::uword i = 0; i <= j; i++) {
      tmp += trimat.at(i, j) * x.at(i);
    }
    x.at(j) = tmp;
  }
}

